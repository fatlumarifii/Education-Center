    public void shtoTeDhenat(Profesori p){
        emritxt.setText(" "+p.getEmri());
        mbiemritxt.setText(" "+p.getMbiemri());
        DateFormat sd = new SimpleDateFormat("dd-MM-yyyy");
        ditelindjatxt.setText(" "+sd.format(p.getDateLindja()));
        qytetitxt.setText(" "+p.getQytetiid().getEmriQytetit());
        gjiniatxt.setText(" "+p.getGjiniaid().getLloji());
        List<TelProfesori> tel = null;
        try {
            tel = telR.findByProfesoriId(p.getProfesoriID());
        } catch (TelProfesoriException ex) {
            Logger.getLogger(ProfesoriProfilFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        teltxt.setText(" "+tel.get(0));
        
    }
