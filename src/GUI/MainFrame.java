    private void findPerdoruesi() {
        if (perdoruesi.getRoli().equalsIgnoreCase("Administrat")) {
            List<Adminstrata> adminList = adminR.findAll();
            for (int i = 0; i < adminList.size(); i++) {
                if (adminList.get(i).getEmail().equalsIgnoreCase(perdoruesi.getEmriPerdoruesit())) {
                    admin = adminList.get(i);
                }
            }
        } else if (perdoruesi.getRoli().equalsIgnoreCase("Profesor")) {
            List<Profesori> profList = profR.findAll();
            for (int i = 0; i < profList.size(); i++) {
                if (profList.get(i).getEmail().equalsIgnoreCase(perdoruesi.getEmriPerdoruesit())) {
                    prof = profList.get(i);
                }
            }
        }
    }

