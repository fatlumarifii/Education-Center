    private void ShkarkobtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShkarkobtnActionPerformed
        JFileChooser fch = new JFileChooser();
        fch.setPreferredSize(new Dimension(700, 500));
        fch.setDialogTitle("Shkarko File-in");
        fch.setSelectedFile(new File(emritxt.getText()));
        int returnVal = fch.showSaveDialog(null);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                FileOutputStream fs = new FileOutputStream(fch.getSelectedFile().getAbsolutePath() + ext);
                fs.write(array);
                fs.close();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(ArkivaFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(ArkivaFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "E dhÃ«na Ã«shtÃ« shkarkuar me sukses !");
            Object[] ob = {"Po", "Jo"};
            int i = javax.swing.JOptionPane.showOptionDialog(this, "A dÃ«shironi ta hapni fajllin e shkarkuar ?", "Hapja", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if (i == 0) {
                Desktop desktop = Desktop.getDesktop();
                File file = new File(fch.getSelectedFile().getAbsolutePath() + ext);
                try {
                    desktop.open(file);
                } catch (IOException ex) {
                    Logger.getLogger(ArkivaFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_ShkarkobtnActionPerformed

